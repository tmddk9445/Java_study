package j01_출력;

public class StudentInfo {

	public static void main(String[] args) {
		/*
		 * 	이름: 김준일
		 * 	학년: 1
		 * 
		 * 	이름: 김준이
		 * 	학년: 1
		 * 	
		 * 	이름: 김준삼
		 * 	학년: 1
		 * 
		 */
		int year;
		year = 2;
		
		System.out.print("이름: ");
		System.out.println("김준일");
		System.out.print("학년: ");
		System.out.println(year);
		System.out.println();
		
		System.out.print("이름: ");
		System.out.println("김준이");
		System.out.print("학년: ");
		System.out.println(year);
		System.out.println();
		
		System.out.print("이름: ");
		System.out.println("김준삼");
		System.out.print("학년: ");
		System.out.println(year);
		System.out.println();
	}

}
